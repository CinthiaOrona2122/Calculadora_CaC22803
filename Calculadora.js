class Calculadora {
   
    //Operaciones con 2 numeros

    sumar(num1, num2) {
        return eval(num1 + num2);
    }

    restar(num1, num2) {
        return eval(num1 - num2);
    }

    dividir(num1, num2) {
        return eval(num1 / num2);
    }

    multiplicar(num1, num2) {
        return eval(num1 * num2);
    }


} 
